/* 
TEMPLATE LITERALS: 
    -> concatenar strings de forma mais simples; 
    -> colocar variaveis entre os textos; 
*/

const userName = 'Carl'
const age = 33

//sem template literals
console.log("Usando sem Template Literals -> Nome: " + userName + " idade: " + age + " anos.")

//usando template literals
console.log(`Usando com Template Literals -> Nome: ${userName} idade: ${age} anos.`)